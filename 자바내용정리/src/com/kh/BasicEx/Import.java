/*
 import
  	 다른 패키지(폴더)에 작성된 클래스가 필요할 때 필요한 구문
  	 
  	 사용예제
  	 	import 패키지명.클래스명;
  	 	
  	 	
  	 	import java.Util.List;
  	 	import java.Util.HashMap;
  	 	import java.Util.ArrayList;
  	 	java 폴더 밑에 Util 폴더 안에 있는 ArrayList 파일을 사용한다는 의미
  	 	똑같은 폴더 경로 위치라고 해도 가져오는 것을 선택해서 갖고 오길 원한다면
  	 	위처럼 가지고 와서 사용할 수 있음 
  	 	
  	 	import java.Util.*;
  	 	java 폴더 밑에 Util 폴더 안에 있는 모든 파일을 가져올 수 있음을 의미
  	 	 * 를 쓴다하여 모든 파일을 가지고 오게되면 프로그램 속도가 낮아질 수 있기 때문에
  	 	모두 가져오는 것이 아니라 가져올 수 있는 준비가 완료된 것임  
  	 	 
  	 	 
 */
